#program to calculate discriminant value
a=float(input("Enter the first value point a: "))
b=float(input("Enter the second value point b: "))
c=float(input("Enter the third value point c: "))
#calculate discriminant value
dis=(b**2)-4*(a*c)
#to display
print("discriminant value is",dis)

